"use client";
import React from "react";
import { motion } from "framer-motion";
import { useCart } from "@/lib/cart-context";
import Link from "next/link";

interface CartIconProps {
  locale?: string;
}

export default function CartIcon({ locale = "es" }: CartIconProps) {
  const { totalItems } = useCart();

  return (
    <Link href={`/${locale}/checkout`}>
      <motion.div
        whileHover={{ scale: 1.1 }}
        whileTap={{ scale: 0.95 }}
        className="relative cursor-pointer"
      >
        <div className="w-12 h-12 rounded-full bg-white/90 backdrop-blur-sm shadow-lg border border-white/20 flex items-center justify-center hover:bg-white transition-all">
          <svg 
            className="w-6 h-6 text-secondary-700" 
            fill="none" 
            stroke="currentColor" 
            viewBox="0 0 24 24"
          >
            <path 
              strokeLinecap="round" 
              strokeLinejoin="round" 
              strokeWidth={2} 
              d="M3 3h2l.4 2M7 13h10l4-8H5.4M7 13L5.4 5M7 13l-2.293 2.293c-.63.63-.184 1.707.707 1.707H17m0 0a2 2 0 100 4 2 2 0 000-4zm-8 2a2 2 0 11-4 0 2 2 0 014 0z" 
            />
          </svg>
          
          {/* Counter Badge */}
          {totalItems > 0 && (
            <motion.div
              initial={{ scale: 0 }}
              animate={{ scale: 1 }}
              className="absolute -top-1 -right-1 w-6 h-6 bg-primary-600 text-white rounded-full flex items-center justify-center text-xs font-bold shadow-lg"
            >
              {totalItems > 99 ? '99+' : totalItems}
            </motion.div>
          )}
        </div>
      </motion.div>
    </Link>
  );
}
